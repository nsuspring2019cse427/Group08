        <h1>Breakfast</h1>
        <p> 

Older adults may have a difficult time consuming adequate nutrition due to decreased absorption efficiency or high cost of healthy foods. Therefore, seniors are at an increased risk for malnutrition, which can decrease their quality of life. According to the American Academy of Family Physicians, up to 60 percent of home-care elderly patients are malnourished or at risk for becoming malnourished. Inexpensive, soft, easy-to-prepare breakfast foods are ideal for seniors.</p>
<h4>Eggs</h4>

<p>Eggs make an excellent breakfast food for seniors. A review published in a 2008 issue of “Clinical Nutrition” found that elderly adults may require up to 1.5 grams of protein per kilogram of body weight, or about 0.68 grams of protein per pound, each day, which is almost two times higher than the current protein RDA. Another review published in a 2004 issue of the “Journal of the American College of Nutrition” reports that eggs are ideal for older adults because they are high in protein, easy to prepare and inexpensive. This review also reports that later in life, high blood cholesterol decreases as a risk for heart disease. Seniors who have high cholesterol or heart disease should choose egg whites instead of whole eggs, especially if they consume more than four eggs per week.</p>
<h4>Oatmeal</h4>

<p>Oatmeal is an excellent breakfast food for seniors because it is fairly inexpensive, easy to chew, easy to prepare, nutrient dense and high in fiber. The National Digestive Diseases Information Clearinghouse reports that low-fiber diets are a main cause of constipation in older adults. The Institute of Medicine encourages adult men ages 50 and older to consume at least 30 grams of fiber and women ages 50 and older to consume at least 21 grams of fiber each day. One packet of regular instant oatmeal provides about 4 grams of fiber.</p>
<h4>Soft Fresh Fruits</h4>

<p>Fresh fruits are excellent sources of dietary fiber and vitamins for seniors. Soft fresh fruits that are easy to chew and swallow include blueberries, bananas, plums, fresh peaches and kiwi fruit. Sprinkle sliced soft fruits on top of regular oatmeal or breakfast cereal prepared with low-fat milk or soy milk.</p>
<h4>Smoothies</h4>

<p>High-protein smoothies make a quick breakfast for seniors, don’t require chewing and are easy to prepare using a blender. Ingredients commonly used in breakfast protein smoothies include low-fat milk or soy milk, low-fat yogurt or soy yogurt, soft fruits and peanut butter, almond butter or cashew butter.</p>


