<h4>Music for health</h4>

<p>This is a program that has proved to be very popular in many nursing and residential homes.

It is a session where the residents get to listen to music that they can sing along to, relax to, and they have instruments like tambourines for example that they are encouraged to use along to the music.

Music for Health can be very beneficial in that it helps people remember old times, relax, socialise, have fun and also with coordination and gentle exercise in using the instruments. It is important that there is an area this activity can take place where interruptions are kept to a minimum, as the person leading the session needs to keep everyone's attention.
Source: Enrique Dans (wikimedia commons)</p>
<h4>Using the Wii</h4>

<p>Although we don't usually associate elderly people with gaming consoles, this has been a success in many homes brave enough to invest in one!

The advantage of the Wii is the interactive nature of the games, where the participants get some exercise whilst playing, it helps hand to eye coordination and the whole activity can be very sociable. It does require a large screen to play the games on so the residents can see what's happening!</p>
<h4>Chair Aerobics</h4>

<p>This activity must be led by someone who has proper training in health and fitness in the elderly.

It is designed to increase the heart rate, helps keeps joints and limbs supple, uses muscles to keep them fit, aids coordination and is also great fun for everyone taking part.

All residents in nursing and residential homes must be encouraged to take some exercise whatever their mobility is like, and this is a great way to include the less mobile people in an exercise program that everyone can enjoy.</p>
<h4>Musical Entertainment</h4>

<p>There are many musicians out there that make their living by going round residential and nursing homes entertaining the residents. This activity is always very popular, being sociable and fun, and helping to keep memories alert by remembering the songs!

This activity is a must for any self respecting activities timetable!
Source: Paul Townsend (wikimedia commons)</p>
<h4>Reminiscence</h4>

<p>Reminiscence is an activity is particularly beneficial to those who suffer with dementia.

It involves talking about the past, remembering events in peoples lives, jobs they've had, activities they used to enjoy, people they knew or met, the list is endless! This activity is great with a small group of people, and should include items collected from the "old days" such as carbolic soap, that everyone involved will remember and can touch and smell, and helps get the conversation going.

Losing your memory as you get old can be very distressing and frightening. Reminiscence can bring a lot of comfort, even if some of the memories are sad ones, as everyone in the group will talk and comfort each other if they get sad.

The person leading this activity also needs to be able to turn the mood around to remember happy times too. Other things in the "memory box" could include pictures and ration books, anything that will bring back memories of times gone by.</p>
